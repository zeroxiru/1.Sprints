def create_table_of_content(movies_db= top250movies):
    toc = {}
    for movie in movies_db:
        toc[movie['title']] = movie['rank']
    return toc

def top_actors(movies_db = top250movies):
  actors = {}
  for movie in movies_db:
    actor_lst = movie["actors"]
    for actor in actor_lst:
        if actor in actors:
          actors[actor] += 1
        else:
          actors[actor] = 1

  return dict(sorted(actors.items(), key=lambda item: item[1], reverse=  True))


def main():
  print(top_actors(top250movies))
  tab_of_con = create_table_of_content()
  for  title, rank in tab_of_con.items():

    print(f" {title},  {rank}, ")


if __name__ == "__main__":
    main()